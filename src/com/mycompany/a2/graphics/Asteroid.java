package com.mycompany.a2.graphics;

public class Asteroid {
	int iShapeX;
	int iShapeY;
	int width, height;
	int color;
	
	public Asteroid(int iShapeX, int iShapeY, int width, int height, int color) {
		this.iShapeX = iShapeX;
		this.iShapeY = iShapeY;
		this.color = color;
		this.width = width;
		this.height = height;
	}
}
